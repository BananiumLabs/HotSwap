import { Component, EventEmitter } from '@angular/core';
import { RequestOptions } from '@angular/http';
import { Observable, BehaviorSubject } from 'rxjs';
import { Router, ActivatedRoute,  } from '@angular/router';
import { AngularFireStorage } from 'angularfire2/storage';
import { AngularFireDatabase, AngularFireObject } from 'angularfire2/database';
import { MaterializeAction } from 'angular2-materialize';
import { Listing } from 'app/module1/listing';
import { MaterializeModule } from 'angular2-materialize';
import { AngularFireAuth } from 'angularfire2/auth';
import { NgModel } from '@angular/forms';



@Component({
  selector: 'app-module-3-page',
  templateUrl: './module-3-page.component.html',
  styleUrls: ['./module-3-page.component.css'],

})
export class Module3PageComponent {
  entries: any;
  currListing: Listing = new Listing();
  percentage: number = 0;
  search: string = "";
  currentmail: string;
  optTitle: string;
  optTo: string;
  modalActions = new EventEmitter<string | MaterializeAction>();

  itemRef: AngularFireObject<any>;
  item: Observable<any>;
  imageURL: Observable<string>;
  constructor(private storage: AngularFireStorage, db: AngularFireDatabase, private auth: AngularFireAuth, private route: ActivatedRoute,
    private router: Router) {
    
    this.currentmail = this.auth.auth.currentUser.email;
    this.itemRef = db.object('message/' + 'xxxxxxxx-xxxx-4xxx-yxxx-xxxxxxxxxxxx'.replace(/[xy]/g, function (c) {
      var r = Math.random() * 16 | 0, v = c == 'x' ? r : (r & 0x3 | 0x8);
      return v.toString(16);
    }));
    this.item = this.itemRef.valueChanges();
    let itemChange = db.object('message').valueChanges();

    let sub = this.route
      .queryParams
      .subscribe(params => {
        // Defaults to 0 if no query param provided.
        console.log(params);
        this.currListing.title = params.title;
        this.currListing.swap = params.mail;

        let that = this;
        setTimeout(function() {
          if (params.title !== undefined && params.title !== null)
            that.openModal();
        }, 500)
      });

    //Update every 2 seconds
    let that = this;
    setInterval(function update() {
      itemChange.subscribe(data => {
        that.entries = new Array();
        for (let item in data)
          that.entries.push(data[item]);
        // console.log(data);
      });
      return update;
    }, 2000);

  }

  openModal() {
    // populate currListing
    this.currListing.authorid = this.auth.auth.currentUser.displayName;
    this.currListing.authoremail = this.auth.auth.currentUser.email;
    this.modalActions.emit({ action: 'modal', params: ['open'] });
  }
  closeModal() {
    this.modalActions.emit({ action: 'modal', params: ['close'] });
  }

  saveListing(event) {
    var date = new Date();
    this.currListing.listDate = date.toDateString() + ' ' + date.getHours() + ':' + date.getMinutes();
    console.log('Saving Object: ');
    console.log(this.currListing);
    this.itemRef.set(this.currListing);
  }

  fileChange(event) {
    console.log('Upload Started!');
    console.log(event.target.files[0]);
    const file = event.target.files[0];
    const filePath = event.target.files[0].name;
    const task = this.storage.upload(filePath, file);
    // observe percentage changes
    task.percentageChanges().subscribe(data => {
      this.percentage = data;
    })

    // get notified when the download URL is available
    // console.log(task.downloadURL());
    task.downloadURL().subscribe(data => {
      this.currListing.imageRef = data;
      console.log(data);
    });
  }

}
